    public void preencheTabela() {
        AcaoCadastrarEquipe acao = new AcaoCadastrarEquipe();
        List equipes = acao.recuperar();   
        String[] columnNames = { "ID", "Codigo", "Nome" };
        Object[][] data = { {"", "", "", } };
        if (equipes != null && equipes.size() > 0) {
            data = new Object[equipes.size()][3];
            for (int i = 0; i < equipes.size(); i++) {
                Equipe p = (Equipe) equipes.get(i);
                data[i][0] = p.getId();
                data[i][1] = p.getCodigo();
                data[i][2] = p.getNome();
            }
        }
        this.jTable1.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
    }

