private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
    String str = "Digite um id valido.";
    if(txtId.getText().length() > 0)
    {
        Long id = Long.parseLong(txtId.getText());
        AcaoCadastrarEquipe acao = new AcaoCadastrarEquipe();
        Equipe e = acao.recuperar(id);
        if(e != null) {
            if (acao.remover(id)) {
                str = "Equipe removida com sucesso";
                preencheTabela();
            }
            else {
                str = "Equipe nao pode ser removida";
            }
        }
    }
    txtId.setText("");
    JOptionPane alert = new JOptionPane();
    alert.showMessageDialog (null, str);
}//GEN-LAST:event_btnDelActionPerformed

